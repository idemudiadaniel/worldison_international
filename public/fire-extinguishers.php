          <p><strong>At Worldison International Ltd, we protect what matters most.</strong></p>
          <p>
            Fire safety is non-negotiable — and a reliable fire extinguisher can mean the difference between
            a controlled incident and a devastating loss. We specialize in supplying, servicing, and recharging
            fire extinguishers to keep homes, businesses, and industries fully prepared for emergencies.
          </p>
          <h2>What we do at Worldison International Ltd</h2>
          <h3>1. Supply of Fire Extinguishers</h3>
          <p>We provide high-quality, standards-compliant extinguishers suitable for all fire classes, including:</p>
          <ul>
            <li><strong>CO₂ (Carbon Dioxide):</strong> For electrical & flammable liquid fires.</li>
            <li><strong>DCP (Dry Chemical Powder):</strong> For general-purpose use, effective on multiple fire types.</li>
            <li><strong>Foam Extinguishers:</strong> For flammable liquids & solid combustibles.</li>
            <li><strong>Wet Chemical Extinguisher:</strong> For kitchens & cooking oil fires.</li>
            <li><strong>Water Extinguishers:</strong> For solid material fires.</li>
          </ul>
          <p>✅ Available in 1kg, 5kg, 8kg, 9kg, and 50kg wheeled units to suit different Applications.</p>

          <h3>2. Servicing of Fire Extinguishers</h3>
          <p> We ensure Your extinguishers are always in optimal condition through: </p>
          <ul>
            <li>Routine inspections pressure, weight, hose, nozzle, seals.</li>
            <li>Maintenance and replacement of damaged parts.</li>
            <li>Hydrostatic testing for cylinder integrity.</li>
            <li> Detailed Fire Extinguisher Service certificates for compliance.</li>
          </ul>

          <h3>3. Recharging of Fire Extinguishers</h3>
          <p> After use or pressure loss, our certified team recharges extinguishers to full operational capacity, ensuring: </p>
          <ul>
            <li>Correct extinguishing agent is used.</li>
            <li>Full pressure restoration and sealing.</li>
            <li>Safety checks checks before returning to service.</li>
          </ul>

